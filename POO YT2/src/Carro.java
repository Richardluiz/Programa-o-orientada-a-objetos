public class Carro {

    private int rodas = 4;
    private int portas = 3;

    public Carro(int rodas, int portas){
        this.rodas = rodas;
        this.portas = portas;
    }

    public int getRodas(){
        return rodas;
    }
    public int getPortas(){
        return portas;
    }
}
